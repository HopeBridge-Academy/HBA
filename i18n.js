function updateContent() {
    document.querySelectorAll('[data-i18n]').forEach(element => {
        element.textContent = i18next.t(element.getAttribute('data-i18n'));
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        element.placeholder = i18next.t(element.getAttribute('data-i18n-placeholder'));
    });
    document.title = i18next.t(document.querySelector('title').getAttribute('data-i18n'));
    document.body.classList.toggle('rtl', i18next.language === 'fa');
}

window.changeLanguage = (lang) => {
    i18next.changeLanguage(lang, () => {
        localStorage.setItem('language', lang);
        updateContent();
    });
};